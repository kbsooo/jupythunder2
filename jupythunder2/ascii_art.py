"""ASCII 아트와 관련된 유틸리티 모듈."""

_SPLASH = r"""
      _                            _   _                         _                 
     | |                          | | | |                       | |                
     | | ___  _   _ _ __ ___   ___| |_| |__  _ __   ___ _ __ ___| |_ _   _ _ __ ___
 _   | |/ _ \| | | | '_ ` _ \ / _ \ __| '_ \| '_ \ / _ \ '__/ __| __| | | | '__/ _ \
| |__| | (_) | |_| | | | | | |  __/ |_| | | | |_) |  __/ |  \__ \ |_| |_| | | |  __/
 \____/ \___/ \__,_|_| |_| |_|\___|\__|_| |_| .__/ \___|_|  |___/\__|\__,_|_|  \___|
                                           | |                                       
                                           |_|                                       
"""


def render_splash() -> str:
    """시작 화면에 사용할 ASCII 아트를 반환한다."""
    return _SPLASH.rstrip()
